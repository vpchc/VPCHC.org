      <h1>Congressman to Visit Parke County Health Center</h1>
      <p>August 9, 2013</p>
      <p>Vermillion-Parke Community Health Center (VPCHC) will celebrated National Health Center Week with a special visit from Congressman Buschon at the VPCHC Bloomingdale health center on Wednesday, August 14th. </p>
      <p>The Bloomingdale health center provides services including primary medical care, preventive and acute care, chronic disease care, well child and sick child visits, as well as behavioral health care services. </p>
      <p> &ldquo;It is very rewarding to work in a community where our services are so much needed.&nbsp; I feel we are providing care in the Bloomingdale area for many underserved individuals who otherwise would find it difficult or impossible to access quality health services.&nbsp; Being a community health center allows us to provide those services,&rdquo; stated VPCHC Chief Medical Officer, Dr. Eric Beachy. Dr. Beachy is currently serving patients in Bloomingdale.</p>
      <p>Congressman Buschon will be providing insight into healthcare from a provider&rsquo;s perspective, as a physician himself. &nbsp;This visit is intended to highlight to the Congressman the federal funding that is being utilized in his district.</p>
      <p>The Bloomingdale health center is accepting new patients and is open Monday - Friday, from 8:00 a.m. to 5:00 p.m., and is located at 201 W. Academy St., Bloomingdale, IN.  If you need assistance or would like to schedule an appointment, please call the health center at (765) 498-9000.</p>
      <p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC provides primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties, and surrounding communities.</p>
