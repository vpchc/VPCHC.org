      <h1>Nurse Practitioner Joins Health Center</h1>
      <p>February 9, 2015</p>
      <p>Valley Professionals Community Health Center (VPCHC) is pleased to announce the addition of Family Nurse Practitioner, Renae Norman, to its medical staff. Norman became part of Valley Professionals during the fall of 2014.</p>
      <p>A graduate of Indiana Wesleyan University, Norman holds a Master of Science in Nursing degree and also completed her bachelor&rsquo;s degree in nursing at Indiana Wesleyan. </p>
      <p>Norman has worked in a variety of clinical settings, where she has gained experience and opportunities to provide care to many patients.&nbsp; Prior to joining VPCHC, her clinical experience includes working as a registered nurse at Provena United Samaritans Medical Center in Danville, Ill., director of nursing at The Waters of Covington in Covington, Ind., and a nurse practitioner at St. Vincent&rsquo;s South Clinic in Veedersburg, Ind. </p>
      <p>A member of the American Nurses Association and Sigma Theta Tau International, Norman is also certified through the American Nurses Credentialing Center. She is a preceptor for Adult and Family Nurse Practitioner students at Ball State University, Purdue University, and the University of Indianapolis.</p>
      <p>Norman enjoys practicing family medicine because it allows her to get to know her patients and develop a relationship with them. &ldquo;By having that patient and provider connection, I am better able to deliver acute, chronic and preventive care to my patients,&rdquo; stated Norman. </p>
      <p>In addition to practicing medicine, Norman enjoys spending time with her husband and their three children.&nbsp; As her children are very active in athletics, she spends a lot of time watching and supporting the North Vermillion Falcons. </p>
      <p>Norman will be see patients at the VPCHC Cayuga location.&nbsp; She is excited to be a part the VPCHC team and enjoys serving the community and providing patient care to new and current patients. </p>
      <p>The Cayuga health center is open Monday - Friday from 8:00 a.m. to 5:00 p.m. If you need assistance or would like to schedule an appointment, please call the Cayuga office at (765) 492-9042. </p>
      <p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive integrated health care for all individuals while promoting educational opportunities. VPCHC provides primary care, behavioral health care, and dental services to the citizens of Vermillion and Parke counties, and surrounding communities. </p>
