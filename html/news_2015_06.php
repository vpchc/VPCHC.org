      <h1>Doctor Joins Health Center</h1>
      <p>February 23, 2015</p>
      <p>Valley Professionals Community Health Center (VPCHC) is pleased to announce the addition of family physician, Dr. Steven Macke, M.D., to its medical staff. Macke joined Valley Professionals in December 2014. </p>
      <p>Macke is a graduate of the University of Illinois College of Medicine and completed his undergraduate studies at Eastern Illinois University. He completed his medical residency training through Rockford Family Medicine Residency Program in Rockford, IL.</p>
      <p>While having worked in a variety of clinical settings, Macke has gained experience and opportunities to provide care to many patients.&nbsp; Prior to joining VPCHC, his clinical experience includes working as a family practitioner at Paris Community Hospital in Paris, Ill., Carle Foundation Physicians in Mattoon Ill., and Cork Medical Center in Marshall, Ill..</p>
      <p>Macke enjoys practicing family medicine because it allows him to provide medical care to a wide range of patients. &ldquo;All of my patients vary in age and the medical problems they experience. I have the opportunity to provide patient care and treatment for different medical issues, to the many people who need it.&rdquo;&nbsp; </p>
      <p>In addition to practicing medicine, Macke enjoys spending time with his wife, children and grandchildren, as well as being a member of the Rotary Club.</p>
      <p>Macke will be seeing patients at the VPCHC Clinton location.&nbsp; He is excited to be part of the VPCHC team and looks forward to serving the community and providing patient care to new and current patients. </p>
      <p>The Clinton health center is open Monday - Friday, from 8:00 a.m. to 5:00 p.m. After-Hours Clinic services are available Monday &ndash; Thursday, from 5:00 p.m. to 8:00 p.m. If you need assistance or would like to schedule an appointment, please call the Clinton office at (765) 828-1003. For more information, visit <a href="http://www.vpchc.org">www.vpchc.org</a>.</p>
      <p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive integrated health care for all individuals while promoting educational opportunities. VPCHC provides primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties, and surrounding communities. </p>
