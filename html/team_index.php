<script>
var flag=1;//There is an issue with it not seeing display=none so you have to click 2 times without this
function expandFun(expandLine){
  var toggleElem;
  var lineSign;
  if(expandLine.id=="teamadmintitle"){
    toggleElem=document.getElementById("teamadminsubsection");
  }else if(expandLine.id=="teammptitle"){
     toggleElem=document.getElementById("teammpsubsection");
  }else if(expandLine.id=="teambptitle"){
	 toggleElem=document.getElementById("teambpsubsection");
  }else if(expandLine.id=="teamchwtitle"){
	 toggleElem=document.getElementById("teamchwsubsection");
  }else if(expandLine.id=="teamfronttitle"){
	 toggleElem=document.getElementById("teamfrontsubsection");
  }else{
	 toggleElem=document.getElementById("teamclinsubsection");
  }
  if(toggleElem.style.display=="none" | flag==1 | toggleElem.style.display==""){
	 toggleElem.style.display="block";
	 lineSign=expandLine.innerHTML;
	 lineSign=lineSign.replace("+","-");
	 expandLine.innerHTML=lineSign;
	 flag=0;
  }else{
	 toggleElem.style.display="none";
	 lineSign=expandLine.innerHTML;
	 lineSign=lineSign.replace("-","+");
	 expandLine.innerHTML=lineSign;
  }
}
</script>
