      <h1>New Mascot is Face of Student Health Care </h1>
      <p>February 10, 2014</p>
      <p>The Valley Professionals Community Health Center (VPCHC) recently welcomed new mascot, &ldquo;Patches the Penguin,&rdquo; to its staff. Patches will be a new face and symbol of student health care through the VPCHC Mobile School-Based Health Center, providing health care resources for students and families.</p>
      <p>Patches likes to exercise, be healthy, meet new people, and is always full of energy.  While accessorized with band aids and stethoscope, Patches encourages students to visit the mobile health center when it visits their school, for any health care services they may need. Services provided through the Mobile School-Based Health Center include care provided by a Nurse Practitioner, for acute symptoms such as cold, flu, cough, fever, as well as immunizations and routine well-child checks. </p>
      <p>Are you ready to meet Patches? Patches will be bringing some added fun and excitement to the mobile health center, local community events, while also making occasional visits to schools and greeting students. The penguin mascot will be a resource for both students and their families, by providing health education and information for community resources and assistance. </p>
      <p>In addition to Patches the mascot, a Community Health Worker from Valley Professionals Community Health Center is available to meet with parents or families to review health insurance enrollment, including guidance for the Affordable Care Act, Medicaid enrollment, and also provide other resources.</p>
      <p>To find out more information about local community resources and assistance, or health care enrollment, please call (765) 828-1003 to schedule an appointment with a Community Health Worker.</p>
      <p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC provides primary and preventive health care services, along with behavioral health services.  VPCHC accepts Medicaid, Medicare, and commercial insurance.</p>
