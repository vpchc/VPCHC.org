      <h1>Health Center Earns Program Recognition</h1>
      <p>September 16, 2013</p>
      <p>Valley Professionals Community Health Center (VPCHC) recently received recognition through the National Committee for Quality Assurance (NCQA) as a Patient Centered Medical Home with a Distinction in Patient Experience Reporting. NCQA Patient Centered Medical Home (PCMH) Recognition is the most widely-adopted model for transforming primary care practices into medical homes for patients. </p>
      <p><img src="../images/photos_2013_06_01.jpg" width="640" height="410" /></p>
      <p>The NCQA&rsquo;s  Patient Centered Medical Home is an innovative program for improving primary care and is structured as a health care setting that facilitates partnerships between individual patients and their personal physicians, and when appropriate, the patient&rsquo;s family. </p>
      <p>In order to receive this recognition, VPCHC met rigorous standards set forth by NCQA concerning patient access to care, care coordination, continuity of care and accountability, as well as quality and safety. This 12-18 month long project included the review of all clinic policies and procedures, as well as the creation and review of many reports on performance outcomes. Some changes that our patient&rsquo;s may notice include increased access through same day appointment availability and the after-hours clinic, patient education on disease management and assistance with self-management of chronic conditions. </p>
      <p>&ldquo;This recognition reflects the culmination of a lot of hard work and dedication by our wonderful employees.  We are proud to be the only certified patient centered medical home in western Indiana,&rdquo; stated Elizabeth Burrows, CEO of the health center.</p>
      <p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC provides primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties, and surrounding communities. </p>
