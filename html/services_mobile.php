      <h3>MSBHC Services:</h3
      <p>The MSBHC provides scheduled, on-site access during the academic day when school is in session. <br /> Services include: <ul>
       <li>immunizations</li> <li>school physicals</li> <li>well-child visits</li> <li>treating cold</li> <li>fever</li> <li>congestion</li> <li>vomiting</li> <li>various other symptoms</li>
       </ul> It is staffed by a Family Nurse Practitioner and nurse. In addition, the mobile health center has also been providing occupational health services for employers in the local communities, including in Parke, Vermillion and Vigo counties.</p>
	  <h3>MSBHC History:</h3>
      <p>In July 2011, the Department of Health and Human Services announced that the Valley Professionals Community Health Center (VPCHC) was awarded a School-Based Capital Program grant. This grant is made possible by the Affordable Care Act, which helps clinics expand and provide more health care services at schools nationwide.</p>
      <p>The purpose and goal of VPCHC&rsquo;s School-Based Capital grant project is to create a collaborative partnership between VPCHC and the five school corporations located in Parke and Vermillion counties, to help children stay in school by identifying and addressing health problems that may interfere in the learning process. The MSBHC will deliver a combination of primary health and a variety of other health-related services to the children attending these schools.</p>
      <p>Other primary goals of the MSBHC include:</p>
      <ul>
        <li> Improve access to comprehensive, quality health care services by providing a convenient resource for students to gain access to the health care system, as well as a source for ongoing care for school-age children.</li>
        <li>Reduce the percentage of school-age children who are unable to obtain or delayed in obtaining necessary medical care.</li>
        <li>Implement evidence-based clinical services through the highly-trained staff of VPCHC, along with other community partners.</li>
        <li>Provide training opportunities for students enrolled in courses geared toward the delivery of health and human services, and to create a qualified local healthcare workforce. </li>
      </ul>
      <p>This one-time grant will purchase and fully equip a mobile-based health center that will aid in the delivery of essential services to children attending any school within the five Parke and Vermillion County school corporations.</p>
      <p>The Valley Professionals Community Health Center is collaborating with Union Hospital&rsquo;s Richard G. Lugar Center for Rural Health to implement the project. </p>
