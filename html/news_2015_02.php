      <h1>Health Center Provides as Resource for New Medicaid Plan</h1>
      <p>February 2, 2015</p>
      <p align="left">Valley Professionals Community Health Center (VPCHC) is available to assist individuals and families who are eligible to enroll or need additional information about the Medicaid expansion in Indiana, known as Healthy Indiana Plan or HIP 2.0.</p>
      <p align="left">According to the official Indiana State press release, Governor Mike Pence announced on January 27, 2015, that the State has received approval from the federal government to use an updated version of the Healthy Indiana Plan known as HIP 2.0 instead of Medicaid to offer access to quality health care to 350,000 uninsured Hoosiers. The Family and Social Services Administration (FSSA) will begin taking applications immediately, with coverage to begin on February 1.</p>
      <p align="left">Also according to the State release, HIP 2.0 participants are required to contribute to a POWER account, which they manage like a health savings account and are rewarded for using preventive care.&nbsp; Consequences for no payment towards the account ranges from mandatory co-pays for services to loss of coverage.&nbsp; The plan also includes a co-pay for emergency room use, designed to encourage appropriate use of the emergency room.</p>
      <p align="left">Valley Professionals has Community Health Workers, who are also certified Indiana Navigators, available at all VPCHC locations, allowing them to provide assistance to families or individuals, through information, resources, and application for enrollment for HIP 2.0 and other health coverage programs, such as the Insurance Marketplace.</p>
      <p>Due to approval of the new HIP 2.0 plan, Indiana will end traditional Medicaid for all non-disabled Hoosiers between 19 and 64 years old and will continue to offer the consumer-driven health care plan to the low-income population. </p>
      <p>To contact a Community Health Worker at Valley Professionals, please call (765) 828-3230.&nbsp; In effort to provide additional details and information about the new Medicaid plan to the public, individuals can visit <a href="http://www.IN.gov">www.IN.gov</a> for a variety of resources. </p>
      <p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for all individuals, while promoting educational opportunities. VPCHC provides primary, behavioral, and dental care services to the citizens of Vermillion and Parke counties, and surrounding communities. </p>
