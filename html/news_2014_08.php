      <h1>May is National High Blood Pressure Education Month </h1>
      <p>May 2014</p>
      <p>May is National High Blood Pressure Education Month. This month offers the perfect opportunity to consider our health, learn more about high blood pressure and make a choice to get blood pressure under control.</p>
      <p>High blood pressure, or hypertension, is one of the most common chronic conditions in the U.S. and Indiana and can lead to heart disease and stroke. Over 30 percent of all Hoosiers have high blood pressure. Almost half of those individuals do not have their condition under control, though. Take the time to talk with your physician about what your blood pressure should be. Take your medication and discuss with your doctor any challenges with your medication, as well. </p>
      <p>Many people do not realize they have high blood pressure. It is often called the &ldquo;silent killer&rdquo; because there are usually no signs or symptoms. Anyone can develop high blood pressure, so it is important to have it checked regularly. </p>
      <p>Although some risk factors, such as age, sex and race or ethnicity are beyond our control, other risk factors involve lifestyle choices. During May, make a choice to prevent high blood pressure. Choose one and start slow. You don&rsquo;t have to change everything all at once to improve your health.</p>
      <ul>
        <li><strong>Eat a healthy diet.</strong> Choose foods that are low in saturated fat and cholesterol <br />
          and high in fiber. Limit salt or sodium in your diet.<br />
          <br />
        </li>
        <li><strong>Maintain a healthy weight.</strong> Modest weight loss can improve your overall health.<br />
          <br />
        </li>
        <li><strong>Exercise regularly.</strong> Start slowly and progress to the recommended 30 minutes of moderate activity most days of the week.<br />
          <br />
        </li>
        <li><strong>Don&rsquo;t smoke.</strong> Smoking greatly increases your risk for heart disease. Smokers are two to four times more likely to develop coronary heart disease than non-smokers. Smoking influences cholesterol and blood pressure, as well. <br />
        </li>
      </ul>
      <p>To learn more about high blood pressure, visit the   <a href="http://www.cdc.gov/bloodpressure/"><strong>CDC Blood Pressure</strong></a> site and the   <a href="http://millionhearts.hhs.gov/resources/tools.php"><strong>Million Hearts Campaign</strong></a>. Smokers can get help to stop smoking at 1-800-QUIT-NOW and at  <a href="http://www.quitnowindiana.com"><strong>www.quitnowindiana.com</strong></a>.  To learn more about eating better, moving more, and avoiding tobacco, visit   <a href="http://www.inshapeindiana.org"><strong>INShape Indiana</strong></a>.</p>
