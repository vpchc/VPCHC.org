      <h1>Health Center Celebrating National Health Center Week</h1>
      <p>July 30, 2012</p>
      <p>The Vermillion-Parke Community Health Center will celebrate National Health Center Week, beginning Monday, August 6th through Friday, August 10th. </p>
      <p>In celebrating, Vermillion-Parke Community Health Center (VPCHC) will be present at local school registrations in Parke and Vermillion counties, to provide information about health center services, Mobile School-Based Health Center enrollment, required child and student immunizations, and other information. VPCHC will be present at South Vermillion Middle School on Tuesday, August 7th, from 3:00 &ndash; 7:00 p.m., Rockville Elementary School on Wednesday, August 8th, from 3:00 &ndash; 7:00 p.m., Turkey Run Elementary on August 13th, from 5:00 &ndash; 7:00 p.m. and North Vermillion Elementary on August 13th, from 6:00 &ndash; 7:00 p.m.</p>
      <p>VPCHC will also be partnering with the Village Market of Parke County on Saturday, August 4th, from 8:30 a.m. to noon, to provide blood pressure checks, educational tools and healthy recipes for visitors, along with information about services available at the health center. The Village Market of Parke County is located at 401 E. Ohio St., Rockville, IN. </p>
      <p>National Health Center Week (NHCW) is dedicated to raising awareness about the mission and accomplishments of America&rsquo;s Health Centers and recognizing the service and contributions of Community, Migrant, Homeless, Public Housing, and Mobile School Health Centers in providing access to affordable, high quality, cost-effective health care to medically vulnerable and underserved people in the U.S. </p>
      <p>NHCW also provides an opportunity to focus on the progress that has been made towards fulfilling the goal of the &quot;Access for All America&quot; plan to provide a health care home to 30 million patients.</p>
      <p>If you would like more information about Mobile School-Based Health Center enrollment, please contact the Clinton office (765) 828-1003 or email <a href="mailto:mobile@vpchc.org">mobile@vpchc.org</a>. </p>
      <p>The VPCHC Clinton and Cayuga locations are open Monday through Friday, from 8:00 a.m. to 5:00 p.m. The After-Hours Clinic is open Monday through Thursday, 5:00 p.m. to 8:00 p.m., at the Clinton location. Contact the Clinton office at (765) 828-1003 or 777 S. Main St., Ste 100, Clinton, IN 47842. Contact the Cayuga office at (765) 492-9042 or 114. N. Division St., Cayuga, IN 47928.</p>
