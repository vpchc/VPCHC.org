      <h1>Bloomingdale Health Center to Open</h1>
      <p>January 4, 2013</p>
      <p>The Valley Professionals Community Health Center (VPCHC) is excited to announce the opening of its new Bloomingdale Community Health Center on January 7th, located at 201 W. Academy Street in Bloomingdale, Indiana.</p>
      <p>To schedule an appointment, please call the Bloomingdale health center at (765) 498-9000, during 8:00 a.m. to 5:00 p.m., Monday through Friday. </p>
      <p>&ldquo;We are extremely excited to be expanding access to Bloomingdale and the surrounding areas.&nbsp; Without the support of the Town of Bloomingdale, we would not have been able to expand services.&nbsp; We look forward to serving any and all individuals at our new community health center,&rdquo; stated Elizabeth Burrows, CEO of VPCHC.</p>
      <p>The Bloomingdale health center is comprised of 3,000 square feet and includes state of the art energy efficient features such as a multi-zone HVAC system, high quality insulation values in the roof and walls and energy efficient lighting. In addition, wireless access will be utilized throughout the facility.</p>
      <p>VPCHC was one of three health centers in Indiana to be awarded a New Access Point grant by Health and Human Services (HHS) in June 2012.  The grant is made possible by the health care law to expand community health centers.  The New Access Point funding is a $650,000 grant that VPCHC will receive annually, during the grant cycle. </p>
      <p>The health center partnered with Performance Services, Inc., for the design and construction of the Bloomingdale Community Health Center. Performance Services is an Indianapolis design-build engineering company that specializes in constructing and renovating educational and healthcare facilities to deliver optimal environments through both the Guaranteed Energy Savings Contract and Design-Build procurement methods.</p>
      <p>VPCHC is a Federally Qualified Health Center (FQHC) dedicated to providing comprehensive primary health care and related services for ALL individuals and families. VPCHC provides primary and preventive health care services, along with behavioral health services to the citizens of Vermillion and Parke counties and surrounding communities.</p>
