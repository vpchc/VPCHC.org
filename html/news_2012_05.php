      <h1>Medical Provider Receives Teaching Award</h1>
      <p>August 10, 2012</p>
      <p>The Valley Professionals Community Health Center would like to recognize one of its physicians, Dr. Greg Brock, D.O., who recently received the prestigious 2012 Outstanding Teaching Award presented by the Predoctoral Education Division of the Indiana University Department of Medicine. </p>
      <p>This award is presented to those who have shown to be an exceptional educator, a passionate teacher an engaged provider.  The evaluations given by all the students he precepted this year highlight Dr. Brock&rsquo;s skills as both a clinician and educator.  Student evaluations shared that Dr. Brock adapts to student needs, is a good physician, and is an example to serving the underserved populations. </p>
      <p>As a recipient of the 2012 Outstanding Teaching Award, Dr. Brock has been invited to attend the American Academy of Family Physician Annual Scientific Conference later this fall in Philadelphia. </p>
      <p>The Valley Professionals Community Health Center is delighted that Dr. Brock has received this honor.  &ldquo;We are extremely proud of the incredible work Dr. Brock does to train medical students, medical residents, nurse practitioner students, and others to contribute to our medical community.  He is a prime example of a physician that gives back to their community by training future medical providers,&rdquo; stated Elizabeth Burrows, CEO of VPCHC.</p>
